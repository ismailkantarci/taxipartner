const FALLBACK_LOCALE: LocaleCode = 'de-AT';

let activeLocale: LocalePrefs = SYSTEM_DEFAULT_LOCALE;
const localeSubscribers = new Set<() => void>();

const notify = () => {
  localeSubscribers.forEach(listener => {
    try {
      listener();
    } catch (error) {
      console.error('[i18n] listener error', error);
    }
  });
};

const interpolate = (template: string, values?: Record<string, string | number>) => {
  if (!values) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (match, key) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
};

export const AVAILABLE_LANGUAGES: Array<{ code: LocaleCode; labelKey: string }> = [
  { code: 'de-AT', labelKey: 'app.language.de' },
  { code: 'en-GB', labelKey: 'app.language.en' },
  { code: 'tr-TR', labelKey: 'app.language.tr' },
  { code: 'uk-UA', labelKey: 'app.language.uk' }
];

export const getActiveLocale = () => activeLocale;

export const setActiveLocale = (prefs: LocalePrefs) => {
  activeLocale = {
    ...SYSTEM_DEFAULT_LOCALE,
    ...prefs
  };
  notify();
};

export const translate = (key: string, options?: TranslateOptions) => {
  const dictionaries = [
    bundles[activeLocale.locale as LocaleCode],
    bundles[FALLBACK_LOCALE]
  ].filter(Boolean) as Messages[];

  const template =
    dictionaries.find(dict => Object.prototype.hasOwnProperty.call(dict, key))?.[key] ??
    options?.defaultValue ??
    key;

  return interpolate(template, options?.values);
};

const subscribeLocale = (listener: () => void) => {
  localeSubscribers.add(listener);
  return () => localeSubscribers.delete(listener);
};

const getLocaleSnapshot = () => activeLocale;

export const useLocale = () =>
  useSyncExternalStore(subscribeLocale, getLocaleSnapshot, getLocaleSnapshot);

export const useTranslation = () => {
  const locale = useLocale();
  return useMemo(
    () => ({
      locale,
      t: (key: string, options?: TranslateOptions) => translate(key, options)
    }),
    [locale]
  );
};
